	result += `<style scoped>table,th,td{border: 1px solid black;border-collapse: collapse;}</style>
	<table><tr><th>僵尸类型</th>`;

	for (var row = 1; row <= 5; row++) {
		if (row < lower_row_limit || row > upper_row_limit) continue;
		result += `<th>第${row}行</th>`
	}

	result += "</tr>";

